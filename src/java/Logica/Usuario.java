
package Logica;


public class Usuario {
    
    private String nombre;
    private String edad;
    private String FechaNacimiento;
    private String Cedula;
    private String sexo;
    private String ciudadResidencia;
    private String Telefono;
    private String Email;
    private String name;
    private String Edad;
    private String raza;
    private String Sexo;
    private String tipo;
    private String color;
    private String alimento;


    public Usuario() {
    }

    public Usuario(String nombre, String edad, String FechaNacimiento, String Cedula, String sexo, String ciudadResidencia, String Telefono, String Email, String name, String Edad, String raza, String Sexo, String tipo, String color, String alimento) {
        this.nombre = nombre;
        this.edad = edad;
        this.FechaNacimiento = FechaNacimiento;
        this.Cedula = Cedula;
        this.sexo = sexo;
        this.ciudadResidencia = ciudadResidencia;
        this.Telefono = Telefono;
        this.Email = Email;
        this.name = name;
        this.Edad = Edad;
        this.raza = raza;
        this.Sexo = Sexo;
        this.tipo = tipo;
        this.color = color;
        this.alimento = alimento;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEdad() {
        return edad;
    }

    public void setEdad(String edad) {
        this.edad = edad;
    }

    public String getFechaNacimiento() {
        return FechaNacimiento;
    }

    public void setFechaNacimiento(String FechaNacimiento) {
        this.FechaNacimiento = FechaNacimiento;
    }

    public String getCedula() {
        return Cedula;
    }

    public void setCedula(String Cedula) {
        this.Cedula = Cedula;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getCiudadResidencia() {
        return ciudadResidencia;
    }

    public void setCiudadResidencia(String ciudadResidencia) {
        this.ciudadResidencia = ciudadResidencia;
    }

    public String getTelefono() {
        return Telefono;
    }

    public void setTelefono(String Telefono) {
        this.Telefono = Telefono;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getedad() {
        return Edad;
    }

    public void setedad(String Edad) {
        this.Edad = Edad;
    }

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public String getsexo() {
        return Sexo;
    }

    public void setsexo(String Sexo) {
        this.Sexo = Sexo;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getAlimento() {
        return alimento;
    }

    public void setAlimento(String alimento) {
        this.alimento = alimento;
    }

    
    
}
 

     
    